package etc;

public class operatorPrePost {
    public static void main(String[] args) {
        int i=0;
        int j=0;
        System.out.println("value i= " + i);
        System.out.println("value j= " + j);
        System.out.println("value of ++i ="+ ++i);
        System.out.println("value of j++ ="+ j++);
        System.out.println("value j= " + j);
    }
}
